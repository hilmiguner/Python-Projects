name = "Hilmi"
print(name[0:2])  # It means: Display the variable 'name' from index 0 to index 2 but do not take the index 2.
print(name[1:])  # It means: Display the variable 'name ' from index 1 to end of the string but this time take the last index.
print(name[:3])  # It means: Display the variable 'name ' from beginning of the string  to index 3 but do not take the index 3.
print(name[0:5:2])  # It means: Display the variable 'name' from index 0 to index 5 but just take indexes in 2 steps and do not take index 5.
